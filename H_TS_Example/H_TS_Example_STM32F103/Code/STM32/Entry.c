#include "Peripheral.h"
#include "app_main.h"




/**
 * @brief 开始线程方法
 * @param v ...
 * @return 0
 */
int Thread_Start(void* v){

  //初始化外设
  Peripherals_Init();


  H_TS_StartNewThread(app_main,NULL,vapp_main_StackSize,vapp_main_Priority,0U);

  
  //结束线程
  return 0;
}


static void IdleProcess(void* v){
	//__WFI();
}

/**
 * @brief 初始化入口
 * @return 无
 */
void Entry(){

  //需要在此将系统初始化到可以开始任务调度的状态
  //此时H_TS已经完成了基本初始化(已经使用了H_TS_Init()) 此时可以新建线程 但线程不会立即运行
  //此函数退出后H_TS_StartScheduler()被调用 开始任务调度
  //建议此时不要开启其他涉及到H_TS的中断 在开始任务调度后再打开这些中断

  //传入的函数会在H_TS空闲时被循环调用
  H_TS_SetIdleProcess(IdleProcess);

  //系统初始化
  Peripherals_SysInit();
  

  //开始一个线程
  H_TS_StartNewThread(Thread_Start,NULL,1024,-1,0);

}









