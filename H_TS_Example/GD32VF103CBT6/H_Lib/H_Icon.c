#include "H_Icon.h"




/**
 * @brief 70x40图标绘制
 * @param DrawPoint 打点方法
 * @param v 调用DrawPoint传入的参数
 * @return 无
 */
void H_Icon_Draw_70x40(void(*DrawPoint)(void* v,int X,int Y),void* v){

  int x;
  int y;
  int i;
  int j;

  x=3;
  y=36;
  for(j=0;j<4;j++){
    for(i=0;i<8;i++){
      DrawPoint(v,x+i,y+j);
    }
    x--;
  }

  x=62;
  y=0;
  for(j=0;j<4;j++){
    for(i=0;i<8;i++){
      DrawPoint(v,x+i,y+j);
    }
    x--;
  }

  x=47;
  y=0;
  for(j=0;j<40;j++){
    for(i=0;i<6;i++){
      DrawPoint(v,x+i,y+j);
      DrawPoint(v,x+i+9,y+j);
    }
    x--;
  }

}

