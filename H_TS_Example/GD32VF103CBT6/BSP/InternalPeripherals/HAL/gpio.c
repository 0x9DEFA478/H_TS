#include "gpio.h"
#include "IP_GPIO.h"


void GPIO_Init(){

  
  rcu_periph_clock_enable(RCU_GPIOA);
  rcu_periph_clock_enable(RCU_GPIOC);

  oIP_GPIO_SetOutput_High(GPIOC,13);
  oIP_GPIO_SetOutput_High(GPIOA,1);
  oIP_GPIO_SetOutput_High(GPIOA,2);

  oIP_GPIO_Config(GPIOC,13,vIP_GPIO_Mode_OUT_OD_xMHz|vIP_GPIO_Mode_OUTAF_2MHz);
  oIP_GPIO_Config(GPIOA,1,vIP_GPIO_Mode_OUT_OD_xMHz|vIP_GPIO_Mode_OUTAF_2MHz);
  oIP_GPIO_Config(GPIOA,2,vIP_GPIO_Mode_OUT_OD_xMHz|vIP_GPIO_Mode_OUTAF_2MHz);

}

