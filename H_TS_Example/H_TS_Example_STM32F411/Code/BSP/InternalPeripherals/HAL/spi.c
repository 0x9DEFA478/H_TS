/* SPI1 init function */
void MX_SPI1_Init(void){

  hspi1.Instance = SPI1;
  hspi1.Init.Mode = SPI_MODE_MASTER;
  hspi1.Init.Direction = SPI_DIRECTION_2LINES;
  hspi1.Init.DataSize = SPI_DATASIZE_8BIT;
  hspi1.Init.CLKPolarity = SPI_POLARITY_LOW;
  hspi1.Init.CLKPhase = SPI_PHASE_1EDGE;
  hspi1.Init.NSS = SPI_NSS_SOFT;
  hspi1.Init.BaudRatePrescaler = SPI_BAUDRATEPRESCALER_4;
  hspi1.Init.FirstBit = SPI_FIRSTBIT_MSB;
  hspi1.Init.TIMode = SPI_TIMODE_DISABLE;
  hspi1.Init.CRCCalculation = SPI_CRCCALCULATION_DISABLE;
  hspi1.Init.CRCPolynomial = 10;
  if (HAL_SPI_Init(&hspi1) != HAL_OK)
  {
    Error_Handler();
  }

  
  ip_spi1=SPI_Handle_MemoryMalloc.Malloc(sizeof(IP_SPI));
  ip_spi1_txdma=SPI_Handle_MemoryMalloc.Malloc(sizeof(IP_DMA));
  IP_DMA_Init(ip_spi1_txdma,hspi1.hdmatx->Instance);
  IP_SPI_Init(ip_spi1,ip_spi1_txdma,NULL,hspi1.Instance,vIP_SPI_Feature_isMaster|vIP_SPI_Feature_isITDMA|vIP_SPI_Feature_DMA|vIP_SPI_Feature_Tx);
}

void MX_SPI1_DeInit(){

  HAL_SPI_DeInit(&hspi1);
  SPI_Handle_MemoryMalloc.Free(ip_spi1_txdma);
  SPI_Handle_MemoryMalloc.Free(ip_spi1);
  ip_spi1=NULL;

}

